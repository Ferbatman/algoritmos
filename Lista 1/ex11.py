a = float(input("Insira aqui um numero positivo e maior que zero: "))

quadrado = a ** 2
cubo = a ** 3
raiz_quadrada = a ** 0.5
raiz_cubica = a ** (1/3)

print("O numero {} elevado ao quadrado é igual a {}".format(a, quadrado))

print("O numero {} elevado ao cubo é igual a {}".format(a, cubo))

print("A raiz quadrada do numero {} é igual a {}".format(a, raiz_quadrada))

print("A raiz cubica do numero {} é igual a {}".format(a, raiz_cubica))